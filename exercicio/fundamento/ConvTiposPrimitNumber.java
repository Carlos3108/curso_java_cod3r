package fundamento;

public class ConvTiposPrimitNumber {
    public static void main(String[] args) {
        double a = 1;
        System.out.println(a);

        float b = (float) 1.23456; // explixita (CAST)
        System.out.println(b);

        int c = 4;
        byte d = (byte) c;
        System.out.println(d);
    }
}
